"""
VOTE-EVAL canonical test case schema.

Every eval case, trace, and result is represented here.
This schema is the backbone of the entire eval repository.
"""
from __future__ import annotations

import json
from enum import Enum
from pathlib import Path
from typing import Any, Literal, Optional

from pydantic import BaseModel, Field


# ---------------------------------------------------------------------------
# Risk classification
# ---------------------------------------------------------------------------

class RiskTier(str, Enum):
    P0 = "P0"  # Catastrophic — emergency misrouting, privacy breach → block deployment
    P1 = "P1"  # Severe operational — wrong number, wrong booking → hard fail
    P2 = "P2"  # Moderate — extra clarification turn, mild awkwardness → score penalty
    P3 = "P3"  # Cosmetic — phrasing preference, verbose close → experience modifier only


# ---------------------------------------------------------------------------
# Ground truth
# ---------------------------------------------------------------------------

class GroundTruth(BaseModel):
    """What the agent must do to pass this case."""

    intent: str
    """Primary intent, e.g. 'create_booking', 'cancel_booking', 'leave_message'."""

    slots: dict[str, str] = {}
    """Expected slot values, e.g. {'service': 'haircut', 'date': 'tomorrow'}.
    Values are used for loose matching — exact match not required."""

    expected_tools: list[str] = []
    """Tool names that must be called, e.g. ['check_availability', 'create_booking']."""

    expected_outcome: str = ""
    """Terminal outcome string, e.g. 'booking_confirmed', 'message_taken'."""

    policy_expectations: list[str] = []
    """Human-readable policy rules the agent must follow. Used by LLM judge in Phase 3."""

    forbidden_actions: list[str] = []
    """Things the agent must NOT do, e.g. 'promise same-day service without checking'."""


# ---------------------------------------------------------------------------
# Success criteria (hard gates)
# ---------------------------------------------------------------------------

class SuccessCriteria(BaseModel):
    """
    Hard gates — any failure here means the case fails regardless of style scores.

    Phase 1 activates: must_capture_intent, must_not_hallucinate_slots, must_complete_task.
    must_call_expected_tools is off by default (enabled per-case where strict tool
    sequencing matters, e.g. 'must check availability before booking').
    """

    must_capture_intent: bool = True
    must_not_hallucinate_slots: bool = True
    must_complete_task: bool = True
    must_call_expected_tools: bool = False

    # Per-case named gates (e.g. {"must_not_book_without_availability_check": True})
    custom_gates: dict[str, bool] = {}


# ---------------------------------------------------------------------------
# Mock tool configuration
# ---------------------------------------------------------------------------

class MockToolResponse(BaseModel):
    """Override the default mock response for a specific tool in this case."""

    tool_name: str
    response: dict[str, Any]


# ---------------------------------------------------------------------------
# EvalCase — the canonical test case
# ---------------------------------------------------------------------------

class EvalCase(BaseModel):
    """A single eval case. The caller side is provided; agent side is generated."""

    case_id: str
    """Unique identifier, e.g. 'booking_tradies_001'."""

    description: str
    """One-line description of what this case tests."""

    # Classification
    business_domain: Literal["tradies", "hair_salon", "smb_general", "medical"]
    scenario_type: str
    """e.g. 'appointment_booking', 'reschedule', 'cancel', 'ask_hours',
    'leave_message', 'request_quote'."""

    priority: Literal["high", "medium", "low"]
    risk_tier: RiskTier

    # Caller-side conversation (agent turns are generated by the runner)
    caller_turns: list[str]
    """Ordered list of caller utterances. The runner feeds each through Claude."""

    # Mock tool behaviour for this case (defaults apply when not overridden)
    mock_tool_responses: list[MockToolResponse] = []

    # Expectations
    ground_truth: GroundTruth
    success_criteria: SuccessCriteria = Field(default_factory=SuccessCriteria)

    # Tenant context fed to the skills engine
    tenant_config: dict[str, Any]
    """Must include: business_name, vertical, phone, timezone, services, integrations."""

    # Slicing metadata
    tags: list[str] = []

    @classmethod
    def from_file(cls, path: Path) -> "EvalCase":
        with open(path) as f:
            return cls(**json.load(f))


# ---------------------------------------------------------------------------
# ConversationTurn
# ---------------------------------------------------------------------------

class ConversationTurn(BaseModel):
    role: Literal["caller", "agent"]
    text: str


# ---------------------------------------------------------------------------
# EvalTrace — captured output from running a case
# ---------------------------------------------------------------------------

class EvalTrace(BaseModel):
    """Structured record of what the agent actually did during a replay run."""

    case_id: str

    # Full reconstructed conversation
    conversation: list[ConversationTurn] = []

    # Agent behaviour (extracted by the runner)
    inferred_intent: Optional[str] = None
    extracted_slots: dict[str, str] = {}
    tools_called: list[str] = []
    tool_arguments: list[dict[str, Any]] = []
    final_outcome: Optional[str] = None

    # Raw agent text turns (used by LLM judge in Phase 3)
    agent_turns: list[str] = []

    # Timing
    duration_ms: Optional[int] = None

    # Error (set if runner threw an exception)
    error: Optional[str] = None


# ---------------------------------------------------------------------------
# Dimension score
# ---------------------------------------------------------------------------

class DimensionScore(BaseModel):
    score: float
    """0 = wrong/missing, 1 = partial, 2 = mostly correct, 3 = fully correct."""

    passed_gate: bool
    """True if this dimension meets its hard gate threshold."""

    notes: str = ""


# ---------------------------------------------------------------------------
# EvalResult — final per-case evaluation result
# ---------------------------------------------------------------------------

class EvalResult(BaseModel):
    """Output of the evaluator for a single case."""

    case_id: str
    business_domain: str
    scenario_type: str
    risk_tier: RiskTier

    # Overall pass/fail (all active hard gates must pass)
    passed: bool

    # Hard gate results (binary)
    hard_gate_results: dict[str, bool] = {}

    # Dimension scores (0–3)
    intent_score: DimensionScore
    slot_score: DimensionScore
    tool_score: DimensionScore
    outcome_score: DimensionScore

    # Weighted average of the four dimension scores
    composite_score: float

    errors: list[str] = []
    notes: list[str] = []
